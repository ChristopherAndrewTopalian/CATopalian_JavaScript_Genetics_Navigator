function makeTitleOfApp()
{
    let theTitle = ce('a');
    theTitle.id = 'theTitle';
    theTitle.href = 'https://github.com/ChristopherAndrewTopalian/CATopalian_JavaScript_Genetics_Navigator';
    theTitle.target = '_blank';
    theTitle.textContent = 'CATopalian JavaScript Genetics Navigator';
    theTitle.style.fontFamily = 'Arial';
    theTitle.style.fontSize = '15px';
    theTitle.style.fontWeight = 'bold';
    theTitle.style.textAlign = 'right';
    theTitle.style.lineHeight = 12 + 'px';
    theTitle.style.textDecoration = 'none';
    theTitle.style.margin = '2px';
    return theTitle;
}

